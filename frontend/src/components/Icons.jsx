// Map weather condition string to the right icon component
export function WeatherIcon({ condition, size = 20, className = '' }) {
    const conditionMap = {
        'Clear': SunIcon,
        'Partly Cloudy': CloudSunIcon,
        'Cloudy': CloudIcon,
        'Light Rain': CloudRainIcon,
        'Heavy Rain': CloudRainIcon,
        'Thunderstorm': CloudLightningIcon,
    }
    const Icon = conditionMap[condition] || CloudIcon
    return <Icon size={size} className={className} />
}
